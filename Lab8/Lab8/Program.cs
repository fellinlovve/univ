using System;

// Тип-перечисление (enum) — один из обязательных элементов
public enum ЧастицаТип
{
    Фотон,
    Электрон,
    Протон
}

// Базовый класс
public abstract class ЭлемЧастица
{
    // Свойства базового класса
    public string Имя { get; set; }
    public double Масса { get; set; } // в а.е.м. или кг, по умолчанию — 0
    public ЧастицаТип Тип { get; protected set; }

    // Конструктор базового класса
    protected ЭлемЧастица(string имя, double масса, ЧастицаТип тип)
    {
        Имя = имя;
        Масса = масса;
        Тип = тип;
    }

    // Базовый метод, который будет вызван в производных классах
    public virtual void ОтобразитьИнформацию()
    {
        Console.WriteLine($"Частица: {Имя}, Тип: {Тип}, Масса: {Масса}");
    }

    // Переопределяемое свойство (в производных классах)
    public virtual string ОписаниеЧастицы
    {
        get { return $"Элементарная частица: {Имя}"; }
    }
}

// Производный класс Фотон
public class Фотон : ЭлемЧастица
{
    // Вызов базового конструктора через : base(...)
    public Фотон() : base("Фотон", 0.0, ЧастицаТип.Фотон)
    {
        // Дополнительная инициализация
        Частота = 0.0;
    }

    public double Частота { get; set; }

    // Переопределение метода базового класса
    public override void ОтобразитьИнформацию()
    {
        // Вызов базового метода (обязательный элемент!)
        base.ОтобразитьИнформацию();
        Console.WriteLine($"Частота фотона: {Частота} Гц");
    }

    // Переопределение свойства
    public override string ОписаниеЧастицы
    {
        get { return $"Фотон — квант электромагнитного поля, безмассовая частица."; }
    }
}

// Производный класс Электрон
public class Электрон : ЭлемЧастица
{
    public Электрон() : base("Электрон", 9.1093837e-31, ЧастицаТип.Электрон)
    {
        Заряд = -1.602176634e-19; // в кулонах
    }

    public double Заряд { get; set; }

    public override void ОтобразитьИнформацию()
    {
        base.ОтобразитьИнформацию();
        Console.WriteLine($"Заряд электрона: {Заряд} Кл");
    }

    public override string ОписаниеЧастицы
    {
        get { return $"Электрон — стабильная элементарная частица с отрицательным зарядом."; }
    }
}

// Производный класс Протон
public class Протон : ЭлемЧастица
{
    public Протон() : base("Протон", 1.67262192e-27, ЧастицаТип.Протон)
    {
        Заряд = +1.602176634e-19;
    }

    public double Заряд { get; set; }

    public override void ОтобразитьИнформацию()
    {
        base.ОтобразитьИнформацию();
        Console.WriteLine($"Заряд протона: {Заряд} Кл");
    }

    public override string ОписаниеЧастицы
    {
        get { return $"Протон — стабильная элементарная частица с положительным зарядом, составляющая ядра атомов."; }
    }
}

// Главный класс для демонстрации работы
class Program
{
    static void Main(string[] args)
    {
        Console.WriteLine("=== Демонстрация иерархии элементарных частиц ===\n");

        // Создание объектов
        Фотон фотон = new Фотон { Частота = 5.0e14 };
        Электрон электрон = new Электрон();
        Протон протон = new Протон();

        // Вывод информации через переопределённые методы
        фотон.ОтобразитьИнформацию();
        Console.WriteLine($"Описание: {фотон.ОписаниеЧастицы}\n");

        электрон.ОтобразитьИнформацию();
        Console.WriteLine($"Описание: {электрон.ОписаниеЧастицы}\n");

        протон.ОтобразитьИнформацию();
        Console.WriteLine($"Описание: {протон.ОписаниеЧастицы}\n");

        // Проверка использования перечисления
        Console.WriteLine($"Тип фотона: {фотон.Тип}");
        Console.WriteLine($"Тип электрона: {электрон.Тип}");
        Console.WriteLine($"Тип протона: {протон.Тип}");

        Console.ReadKey();
    }
}